async def do_broadcast(query, context):
    text = context.user_data.get('broadcast_text', '')
    if not text:
        await safe_edit(query, "❌ Текст пустой.", BACK_TO_MAIN)
        _clear_flow(context)
        return

    users = await asyncio.to_thread(db.get_all_users)
    total = len(users)
    sent = failed = 0

    status_msg = await query.edit_message_text(
        f"📤 Рассылка... 0/{total}", parse_mode='HTML'
    )

    for i, (uid, *_) in enumerate(users):
        try:
            await context.bot.send_message(chat_id=uid, text=text, parse_mode='HTML')
            sent += 1
        except (Forbidden, BadRequest):
            failed += 1
        except Exception:
            failed += 1
        if i % 20 == 0:
            await asyncio.sleep(1)
        if i % 10 == 0 or i == total - 1:
            try:
                await status_msg.edit_text(
                    f"📤 Рассылка... {i+1}/{total}  ✅{sent} ❌{failed}",
                    parse_mode='HTML'
                )
            except Exception:
                pass

    _clear_flow(context)
    kb = [[btn("↩️ Админка", 'admin_panel')]]
    await status_msg.edit_text(
        f"✅ <b>Рассылка завершена</b>\n\n✅ Отправлено: {sent}\n❌ Ошибок: {failed}",
        parse_mode='HTML',
        reply_markup=InlineKeyboardMarkup(kb)
    )


