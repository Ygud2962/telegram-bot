async def handle_web_app_data(update: Update, context: CallbackContext):
    """Обрабатывает данные из Mini App игры."""
    if not update.message or not update.message.web_app_data:
        return
    data_str = update.message.web_app_data.data
    try:
        data = json.loads(data_str)
    except Exception:
        logger.warning(f"handle_web_app_data: bad JSON: {data_str[:100]}")
        return

    user = update.effective_user
    if not user:
        return
    event_type  = data.get('type', '')
    # 'sync' — синхронизация из профиля, обрабатываем как chapter_complete
    if event_type == 'sync':
        event_type = 'chapter_complete'
    user_name   = user.first_name + (f" {user.last_name}" if user.last_name else "")
    chapter     = data.get('chapter', 0)
    score       = data.get('score', 0)
    total_score = data.get('total_score', 0)
    completed   = data.get('completed', chapter)
    game_over   = data.get('game_over', False)
    failed      = (event_type == 'chapter_failed')

    # Сохраняем в БД + проверяем роль параллельно
    _, current_role = await asyncio.gather(
        asyncio.to_thread(
            db.save_game_result,
            user.id, user_name, chapter, score, total_score,
            completed, game_over, failed
        ),
        asyncio.to_thread(db.get_game_role, user.id),
    )
    logger.info(f"game_result saved: user={user.id} ({user_name}), type={event_type}, "
                f"chapter={chapter}, score={score}, total={total_score}, completed={completed}")

    # Автоматически назначаем роль
    if not current_role or current_role == 'player':
        if await is_bot_admin_async(user.id):
            await asyncio.to_thread(db.set_game_role, user.id, 'admin')
        else:
            await asyncio.to_thread(db.set_game_role, user.id, 'player')

    if event_type == 'chapter_complete':
        ch_title = data.get('chapter_title', f'Глава {chapter}')
        await update.message.reply_text(
            f"🔐 <b>Глава завершена!</b>\n\n"
            f"📖 {ch_title}\n"
            f"⭐ <b>{score} очков</b> за главу\n"
            f"📊 Всего: <b>{total_score} очков</b>\n\n"
            f"{'🏆 Все главы пройдены!' if game_over else 'Продолжай — следующая глава ждёт!'}",
            parse_mode='HTML'
        )
        if game_over:
            for a in (await get_admin_ids()):
                try:
                    await context.bot.send_message(
                        chat_id=a,
                        text=f"🏆 <b>{user_name}</b> прошёл Шивровальщика!\nОчков: <b>{total_score}</b>",
                        parse_mode='HTML'
                    )
                except Exception:
                    pass

    elif event_type == 'chapter_failed':
        ch_title = data.get('chapter_title', f'Глава {chapter}')
        await update.message.reply_text(
            f"💔 <b>Глава не пройдена</b>\n\n"
            f"📖 {ch_title}\n"
            f"⭐ Очков за главу: <b>{score}</b>\n"
            f"📊 Всего: <b>{total_score} очков</b>\n\n"
            f"<i>Ничего страшного — впереди ещё новые задания!</i>",
            parse_mode='HTML'
        )

    elif event_type == 'game_complete':
        rank = data.get('rank', '')
        pct  = data.get('pct', 0)
        await update.message.reply_text(
            f"🏆 <b>ИГРА ПРОЙДЕНА!</b>\n\n"
            f"👤 {user_name}\n"
            f"⭐ Итого: <b>{total_score} очков</b> ({pct}% от максимума)\n"
            f"🎖 Звание: <b>{rank}</b>\n\n"
            f"Результат сохранён в таблице лидеров школы!",
            parse_mode='HTML'
        )
        for a in (await get_admin_ids()):
            try:
                await context.bot.send_message(
                    chat_id=a,
                    text=f"🏆 <b>{user_name}</b> завершил Шивровальщика!\n"
                         f"Очков: <b>{total_score}</b> · {rank}",
                    parse_mode='HTML'
                )
            except Exception:
                pass


