async def global_error_handler(update: object, context: CallbackContext):
    """Глобальный обработчик ошибок."""
    import traceback
    from telegram.error import Conflict, NetworkError, TimedOut

    err = context.error

    # Conflict — два экземпляра при редеплое Railway
    # Это нормально: старый контейнер ещё жив пока новый стартует
    if isinstance(err, Conflict):
        logger.warning(f"Конфликт getUpdates (редеплой): {err}")
        # Ждём немного и пробуем продолжить — новый экземпляр победит
        await asyncio.sleep(2)
        return

    # Сетевые таймауты — тоже не спамим админу
    if isinstance(err, (NetworkError, TimedOut)):
        logger.warning(f"Сетевая ошибка: {err}")
        return

    # Ошибки БД — логируем, не спамим админу при каждом запросе
    import psycopg2
    if isinstance(err, (psycopg2.OperationalError, psycopg2.InterfaceError)):
        logger.error(f"❌ Ошибка БД: {err}")
        # Сообщаем пользователю
        try:
            if isinstance(update, Update):
                msg = txt.ERR_DB_TEMP
                if update.callback_query:
                    await update.callback_query.answer(msg[:200])
                elif update.message:
                    await update.message.reply_text(msg)
        except Exception:
            pass
        return

    tb = ''.join(traceback.format_exception(type(err), err, err.__traceback__))
    logger.error(f"Необработанная ошибка:\n{tb}")

    short = str(err)[:300]
    for admin_id in (await get_admin_ids()):
        try:
            await context.bot.send_message(
                chat_id=admin_id,
                text=f"⚠️ <b>Ошибка в боте</b>\n\n<code>{short}</code>",
                parse_mode='HTML'
            )
        except Exception:
            pass

    try:
        if isinstance(update, Update):
            text = txt.ERR_GENERIC
            if update.callback_query:
                try:
                    await update.callback_query.answer("⚠️ Ошибка, попробуйте ещё раз")
                except Exception:
                    pass
                try:
                    await update.callback_query.message.reply_text(text)
                except Exception:
                    pass
            elif update.message:
                await update.message.reply_text(text)
    except Exception:
        pass


