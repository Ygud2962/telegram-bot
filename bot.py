async def set_maintenance(query, context, duration: str):
    now = datetime.now(TZ_MINSK)
    until = None
    if duration == '1h':
        until = (now + timedelta(hours=1)).strftime('%d.%m %H:%M')
    elif duration == '3h':
        until = (now + timedelta(hours=3)).strftime('%d.%m %H:%M')
    elif duration == '5h':
        until = (now + timedelta(hours=5)).strftime('%d.%m %H:%M')
    elif duration == 'tomorrow':
        until = (now + timedelta(days=1)).replace(hour=8, minute=0).strftime('%d.%m %H:%M')

    await asyncio.to_thread(db.set_maintenance_mode, True, until, None)
    global _maintenance_cache
    _maintenance_cache = {'enabled': True, 'until': until, 'message': None,
                          'last_check': datetime.min}

    msg = "✅ <b>ТЕХРЕЖИМ ВКЛЮЧЁН</b>\n"
    msg += f"До: {until}\n" if until else "♾️ Бессрочно\n"
    kb = [[btn("↩️ Админка", 'admin_panel')]]
    await safe_edit(query, msg, kb)


