    if not parsed:
        await safe_edit(query, "❌ Нет данных для сохранения.", BACK_TO_MAIN)
        return

    saved = 0
    skipped = 0
    errors = []
    notified = []

    for sub in parsed:
        date_str = sub.get('date', '')
        cls      = str(sub.get('class', '')).lower().strip()
        lesson   = sub.get('lesson', 0)
        subject  = sub.get('subject', 'Неизвестно')
