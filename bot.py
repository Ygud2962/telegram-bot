def _origin_from_url(raw_url: str) -> str:
    if not raw_url:
        return ''
    try:
        parsed = urllib.parse.urlparse(raw_url)
        if parsed.scheme and parsed.netloc:
            return f"{parsed.scheme}://{parsed.netloc}"
    except Exception:
        return ''
    return ''


